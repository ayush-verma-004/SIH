// Function to fetch and display QR code generated by the backend
async function showQRCode() {
    const amount = document.getElementById('amount').value;
    if (!amount) {
        alert("Please enter an amount");
        return;
    }

    // Fetch QR code from backend API
    const response = await fetch(`/generateQR?amount=${amount}`);
    const qrImage = await response.text();
    document.getElementById('qrCode').src = `data:image/png;base64,${qrImage}`;
    document.getElementById('qrCode').style.display = 'block'; // Show the QR image
}
